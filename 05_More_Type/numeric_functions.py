# To find the maximum or minimum of some numbers or a list, you can use
# max or min.
print(min(1, 2, 3, 4, 0, 2, 1))
print(max([1, 4, 9, 2, 5, 6, 8]))

# To find the distance of a number from zero(its absolute value), use abs
print(abs(-99))
print(abs(42))

# To find the total of a list, use sum.
print(sum([1, 2, 3, 4, 5]))

# Don't use it in project 
print(round(3.14159265, 4))
