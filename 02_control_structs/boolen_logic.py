# Python used words for its Boolen operators, whereas most other languages use symbols such as && || !
# and
print(1 == 1 and 2 == 2)
print(1 == 1 and 2 == 3)
print(1 != 1 and 2 == 2)
print(2 < 1 and 3 > 6)

#or
print(1 == 1 or 2 == 2)
print(1 == 1 or 2 == 3)
print(1 != 1 or 2 == 2)
print(2 < 1 or 3 > 6)

# not
print(not 1 == 1)# !True=False
print(not 1 > 7) # !False=True

